#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char c;

	if (n < 0)
	{
		write(fd, "-", 1);
		c = (n % 10) * (-1) + '0';
		n = (n / 10) * -1;
	}
	else
	{
		c = (n % 10) + '0';
		n = n / 10;
	}
	if (n != 0)
		ft_putnbr_fd(n, fd);
	write(fd, &c, 1);
}
